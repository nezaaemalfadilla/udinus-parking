    private void format_tanggal()
    {
        Calendar cal = Calendar.getInstance();
        String DATE_FORMAT = "h:mm a";
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat(DATE_FORMAT);
        Calendar c1 = Calendar.getInstance();
        int jam = c1.get(Calendar.HOUR);
        int menit = c1.get(Calendar.MINUTE);
        int cekWaktu = c1.get(Calendar.AM_PM);
        if(cekWaktu == 1){
           tanggal=Integer.toString(jam)+":"+Integer.toString(menit)+ " PM"; 
        }
        else if(cekWaktu == 0){
            tanggal=Integer.toString(jam)+":"+Integer.toString(menit)+ " AM"; 
        }
    }
    

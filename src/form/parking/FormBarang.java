    private void btn_saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_saveMouseClicked
        try {
            String tKode=idField1.getText();
            String tNama=fildNamaBarang.getText();
            format_tanggal();
            int jumlah_ = Integer.parseInt(fildJml.getText());

            try {
                if (edit == true) {
                    stm.executeUpdate("update barang set namaBarang='"+tNama+"',jumlahBarang="+jumlah_+" where id='" + tKode + "'");
                    JOptionPane.showMessageDialog(null, "Data berhasil dirubah");
                }
                else{
                    stm.executeUpdate("INSERT into barang VALUES('"+tKode+"','"+tNama+"','"+jumlah_+"','"+tanggal+"')");
                    JOptionPane.showMessageDialog(null, "Data berhasil di inputkan");
                }
                tabelModel.setModel(new DefaultTableModel(dataTable,header));
                baca_data();
                setTombol(true);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, e);

            }
           
        }    
         catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Tidak boleh kosong");
        }
        
    }//GEN-LAST:event_btn_saveMouseClicked


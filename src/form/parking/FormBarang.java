    public void baca_data(){
        try{
            stm = Con.createStatement();
            RsBrg = stm.executeQuery("select * from barang");
            
            ResultSetMetaData meta = RsBrg.getMetaData();
            int col = meta.getColumnCount();
            int baris = 0;
            while(RsBrg.next()) {
                baris = RsBrg.getRow();
            }
            
            dataTable = new Object[baris][col];
            int x = 0;
            RsBrg.beforeFirst();
            while(RsBrg.next()) {
                dataTable[x][0] = RsBrg.getString("id");
                dataTable[x][1] = RsBrg.getString("namaBarang");
                dataTable[x][2] = RsBrg.getInt("jumlahBarang");
                dataTable[x][3] = RsBrg.getString("waktuInput");
                x++;
            }
            
            tabelModel.setModel(new DefaultTableModel(dataTable,header));
            }
            catch(SQLException e)
            {
                JOptionPane.showMessageDialog(null, e);
            }
    }

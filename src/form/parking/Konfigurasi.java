    private void data_jenis_kendaraan(){
        try {
            cmbJenisKendaraan.removeAllItems(); // Delete semua
            RsKonfigurasi = stm.executeQuery("select * from config where name = 'jenis_kendaraan'");
            while (RsKonfigurasi.next()) {
                cmbJenisKendaraan.addItem(RsKonfigurasi.getString("value").trim()); // Input ke cmb box
            }
            System.out.println(cmbJenisKendaraan);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void data_kapasitas(){
        try {
            RsKonfigurasi = stm.executeQuery("select * from config where name = 'kapasitas'");
            while (RsKonfigurasi.next()) {
                txtKapasitas.setText(RsKonfigurasi.getString("value"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

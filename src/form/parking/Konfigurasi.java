    private void open_db(){
        try {
            KoneksiMysql konek = new KoneksiMysql();
            Con = konek.getConnection();
            stm = Con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_READ_ONLY);
            System.out.println("Berhasil ");
        } catch (Exception e) {
            System.out.println("Error : " + e);
        }
    }
    
    void baca_data_users() {
        try {
            ResultSet users = stm.executeQuery("select * from users");
            ResultSetMetaData meta = users.getMetaData();
            int col = meta.getColumnCount();
            Integer baris = 0;
            
            while (users.next()) {
                baris = users.getRow();
            }
            Object[][] dataTable = new Object[baris][col];
            int x = 0;
            users.beforeFirst();
            
            while (users.next()) {
                int roles = users.getInt("role");
                dataTable[x][0] = users.getString("nama");
                dataTable[x][1] = users.getString("username");
                dataTable[x][2] = roles == 0 ? "Kepala" : "Pegawai";
                x++;
            }
            String[] header = {"Nama","Username","Role"};
            tabelUserData.setModel(new DefaultTableModel(dataTable, header));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    

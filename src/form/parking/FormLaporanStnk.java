    private void open_db(){
        try {
            KoneksiMysql konek = new KoneksiMysql();
            Con = konek.getConnection();
            stm = Con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_READ_ONLY);
            System.out.println("Berhasil ");
        } catch (Exception e) {
            System.out.println("Error : " + e);
        }
    }
    
    private void get_kode_laporan(){
        try {
            RsLaporan = stm.executeQuery("select * from trx_no_stnk");
            Integer baris = 0;
            
            while (RsLaporan.next()) {
                baris = RsLaporan.getRow();
            }
            baris += 1;
            txtIdLaporan.setText("LP-" + Integer.toString(baris));
            
        }catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }


    private void btnLaporKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_btnLaporKeyPressed
        String kodeLaporan = txtIdLaporan.getText().toString();
        String noKendaraan = txtNoKendaraan.getText().toString();
        String noIdentitas = txtNoIdentitas.getText().toString();
        int jnsIdentitas = cmbIdentitasJaminan.getSelectedIndex();
        try {
            stm.executeUpdate("INSERT into trx_no_stnk (kode_laporan,no_kendaraan,no_identitas,jns_identitas) values('"+kodeLaporan.toUpperCase()+"','"+noKendaraan+"','"+noIdentitas+"',"+jnsIdentitas+")");
            JOptionPane.showMessageDialog(null, "Sukses Input Laporan");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e); 
        }
    }//GEN-LAST:event_btnLaporKeyPressed

